	<script>
       
		function addcart(id) {
			
            var product_id = $("#product_id").val(); 
			var quantity = $("#quantity").val();                    

            $.ajax({
                type: 'post',
                data: {
                    product_id: product_id,                   
                    quantity: quantity,                   
                    _token: "{{ csrf_token() }}"
                },
                url: "{{ url('cart-add') }}",
               	
				success: function (data) {
				window.location = '{{ url('cart') }}'
				},
                

            })
        }
		
</script>
